for (let firstNumber = 1; firstNumber <= 3; firstNumber++) {
  for (let secondNumber = 1; secondNumber <= 3; secondNumber++) {
    const result = firstNumber * secondNumber;
    console.log(`${firstNumber} * ${secondNumber}  = ${result}`);
  }
  console.log("")
}

